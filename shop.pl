isGoldEnough(Gold,Price) :-
    Gold >= Price.

buyItem(Option) :-
    (
        Option =:= 1 ->
        (
            player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,Gold),
            isGoldEnough(Gold,50) -> 
                NewGold is Gold-50,
                addInventory(potion),
                retract(player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,Gold)),
                asserta(player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,NewGold)),
                write('\nYou get potion\n'),!
        ;
        
            player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,Gold),
            \+isGoldEnough(Gold,50) -> 
                write('\nYour gold is not sufficient\n'),!
        )
    ;
    
        Option =:= 2 ->
        (
            player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,Gold),
            isGoldEnough(Gold,200) -> 
                NewGold is Gold-200,
                random(2,21,ID),
                item(ID,ItemName,_,_,_,_,_),
                addInventory(ItemName),
                retract(player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,Gold)),
                asserta(player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,NewGold)),
                (
                    (ItemName = sword ; ItemName = silver_sword ; ItemName = royal_sword ; ItemName = ultimate_sword) ->
                        print_Sword
                ;
                    (ItemName = bow ; ItemName = silver_bow ; ItemName = royal_bow ; ItemName = ultimate_bow) ->
                        print_Bow
                ;
                    (ItemName = wand ; ItemName = silver_wand ; ItemName = royal_wand ; ItemName = ultimate_wand) ->
                        print_Wand
                ;
                    (ItemName = armor ; ItemName = silver_armor ; ItemName = royal_armor ; ItemName = ultimate_armor) ->
                        print_Armor
                ),
                write('\nYou get '),write(ItemName),write('\n'),!
        ;
        
            player(Username,Job,LVL,HP,MaxHP,ATT,DEF,EXP,MaxEXP,Gold),
            \+isGoldEnough(Gold,200) -> 
                write('\nYour gold is not sufficient\n'),!
        )
    ;   
        Option =:= 3 ->
            cancel
    ).

shop :- 
    write('\nWelcome to Shop\n'),
    write('What do you want to buy?\n'),
    write('1. Potion (50)\n'),
    write('2. Gacha Item (200)\n'),
    write('3. Cancel\n'),
    read(Option),
    (
        Option =\= 3 ->
            buyItem(Option),
            shop
    ;
        write('\nThank you for shopping here\n')
    ).
    

